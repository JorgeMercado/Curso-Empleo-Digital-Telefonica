var cont = 0;

function colocar1(cont){
	var celda1=document.getElementById("celda1");
	if (celda1.className == "vacia") {
		if (cont == 0) {
			celda1.className ="cruz";
			cont++;
		}else if(cont == 1){
			celda1.className ="circulo";
			cont--;
		}	
	}
	return cont;
}
