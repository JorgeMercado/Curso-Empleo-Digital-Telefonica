
public class Principal {

	public static void main(String[] args) {
		
		//Bucle while
		int i=0;
		while(i<100){
			System.out.println(i);
			i++;
		}
		
		//Bucle for
		for(int j=0;j<100;j++){
			System.out.println(j);
		}
		
		//Bucle infinito
		for(;;){
			System.out.println("Me he colgado");
		}

	}

}
